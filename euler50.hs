-- problem 48: Find the last ten digits of the series,
-- 1^1 + 2^2 + 3^3 + ... + 1000^1000.


solution48 :: Int
solution48 = read . reverse . take 10 . reverse . show .
             sum $ map (\x -> x^x) [1..1000]


